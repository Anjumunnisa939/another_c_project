#include <stdio.h>
int main(){
    //see here we have 4 datatypes in c    
    //1. int - stores integer values example : 5;and to print these we use %d
    //2. float - stores decimal values example : 5.5;and to print these we use %f
    //3. char - stores single characters example : 'a';and to print these we use %c
    //4. double - stores double precision decimal values example : 5.555555;and to print these we use %lf
    int a =5;
    printf("%d",a);
    float b = 5.5;
    printf("\n%f",b);
    char c = 'a';
    printf("\n%c",c);
    double d = 5.555555;
    printf("\n%lf",d);
    return 0;
}
// i hope this helps you understand datatypes in c